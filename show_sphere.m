clear all;
clc;

[x, y, z] = sphere(20);
subplot(1, 2, 1);
surf(x, y, z);
axis equal;
light('Position', [0, 1, 1]);
shading interp;
hold on;
plot3(0, 1, 1, 'p');
text(0, 1, 1, 'light');
subplot(1, 2, 2);
surf(x, y, z);
axis equal;
light('Position', [1, 0, 1]);
shading interp;
hold on;
plot3(1, 0, 1, 'p');
text(1, 0, 1, 'light');
